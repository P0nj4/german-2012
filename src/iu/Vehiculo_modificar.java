    @Override
    public void update(Observable o, Object arg) {
        try {
            ddlVehiculos.removeAllItems();
            btnAceptar.setEnabled(false);
            ddlVehiculos.addItem("Seleccione un vehículo");
            lista = Fachada.getInstance().listarVehiculos(usuLogged);
            for (int i = 0; i < lista.size(); i++) {
                Vehiculo v = (Vehiculo)lista.get(i);
                ddlVehiculos.addItem(v.toString());
            }
        } catch (Exception ex) {            
            msgBoxError(ex.getMessage());            
        }
    }
