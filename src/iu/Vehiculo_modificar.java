private void ddlVehiculosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ddlVehiculosActionPerformed
    try {
        if (ddlVehiculos.getSelectedIndex() == 0) {
            txtMarca.setText("");
            txtMatricula.setText("");
            txtModelo.setText("");
            btnAceptar.setEnabled(false);
        } else {
            btnAceptar.setEnabled(true);
            if (lista != null) {
                Vehiculo v = ((Vehiculo) lista.get(ddlVehiculos.getSelectedIndex() - 1));
                txtMarca.setText(v.getMarca());
                txtMatricula.setText(v.getMatricula());
                txtModelo.setText(v.getModelo());
            }
        }
    } catch (Exception ex) {
        msgBoxError(ex.getMessage());
    }
}//GEN-LAST:event_ddlVehiculosActionPerformed

